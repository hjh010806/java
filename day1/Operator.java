package day1;

public class Operator {
    public static void main(String[] args) {
        // 연산자

        //산술연산자
        // +, -, *, /, %
        System.out.println(10 + 5);
        System.out.println(10 - 5);
        System.out.println(10 * 3);
        System.out.println(10 / 3);
        System.out.println(10 % 3);


        //비교연산자
        // >, <, >=, <=, ==, !=
        System.out.println(10 > 5);  // 크다
        System.out.println(10 < 5);  // 작다.
        System.out.println(10 >= 5); // 크거나 같다.
        System.out.println(10 <= 5); // 작거나 같다.
        System.out.println(10 == 5); // 같다
        System.out.println(10 != 5); // 같지않다.

        //논리연산자


    }
}
